%loadlifecells loads special patterns for Conway's Game of Life into GRIND
% load one of a large number of patterns for Conways Game of Life, that were
% compiled by Alan Hensel and others. Downloaded from:
% http://www.argentum.freeserve.co.uk/lex_home.htm.
%
% (p1) = still life (period 1)
% (p4) = period 4
%
% usage:
%   loadlifecells (a listbox with all names appears)
%   loadlifecells ecologist - load the pattern "ecologist"
function loadlifecells(apattern)
global g_grind;
if length(g_grind.statevars.vectnames)~=1
    error('grind:life','model does not seem to be life');
else
    siz=[g_grind.statevars.dims{1}.dim1 g_grind.statevars.dims{1}.dim2];
    avar=g_grind.statevars.vectnames{1};
end

tic;
afile='loadlifecells.m';
fid=fopen(afile,'r');
lines=textscan(fid,'%s','delimiter','\n');
lines=lines{1};
fclose(fid);
fnames=find(strncmp(lines,'%:',2));
names=lines(fnames);
fcolon=strfind(names,':');
fTAB=strncmp(lines,sprintf('%%\t'),2);
descr=cell(size(names));
patterns=cell(size(names));
for i=1:length(names)
    s=names{i};
    names{i}=s(fcolon{i}(1)+1:fcolon{i}(2)-1);
    descr{i}=s(fcolon{i}(2)+1:end);
    j=fnames(i)+1;
    while j<length(lines)&&~fTAB(j)
        descr{i}=sprintf('%s\n%s',descr{i},lines{j}(2:end));
        j=j+1;
    end

    pattern=[];
    k=1;
    while j<length(lines)&&fTAB(j)
        pattern(k,:)=lines{j}(3:end);
        k=k+1;
        j=j+1;
    end

    pattern=pattern-46;
    pattern=pattern~=0;
    patterns{i}=pattern;
end

fprintf('Loaded %d patterns from the <a href="http://www.argentum.freeserve.co.uk/lex_home.htm">Game of Life Lexicon</a>\n',length(names));
%fclose(fid);
if nargin>0
    s=find(strcmp(apattern,names));
    OK=1;
else
    s=[];
end
if isempty(s)
[s,OK] = listdlg('PromptString','Select a pattern:',...
                      'SelectionMode','single',...
                      'ListString',names);
end

if OK
    mat=patterns{s};
    fprintf('\n%s:\n',names{s});
    disp(descr{s});
 %   disp('(px) is period x');
mat1=zeros(siz);
siz2=size(mat);
if siz2(1)>siz(1)
    i=1;
else
    i=floor((siz(1)-siz2(1))/2);
end

if siz2(2)>siz(2)
    j=1;
else
    j=floor((siz(2)-siz2(2))/2);
end

mat1(i:i+siz2(1)-1,j:j+siz2(2)-1)=mat;
assignin('base',avar,mat1);

viewcells();

end


